public class Ex05비교연산자 {
	public static void main(String[] args) {
		// 비교 연산자
		// < <= > >=
		// == !=
		
		// == : 같다
		// != : 같지 않니? (비교) 
		
		// 결과값이 참/거짓
		int a = 3;
		int b = 10;
		
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a<b);
		
		
	}
}
